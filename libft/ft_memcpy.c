#include "libft.h"

void	*ft_memcpy(void *dest, const void *src, size_t n)
{
	size_t	i;
	char	*d;
	char	*s;

	if (dest == NULL && src == NULL)
		return (NULL);
	i = -1;
	d = (char *)dest;
	s = (char *)src;
	while (++i < n)
		d[i] = s[i];
	return (dest);
}
